INSERT INTO USRXCAM(ORIGEM.ISOWNER, ORIGEM.NOMCAM, DESTINO.NOME, ORIGEM.NOMTAB, DESTINO.CODCFGUSR, ORIGEM.CODNOVOSCAMPOS) 

SELECT  ORIGEM.ISOWNER
      , ORIGEM.NOMCAM
      , DESTINO.NOME
      , ORIGEM.NOMTAB
      , DESTINO.CODCFGUSR
      , ORIGEM.CODNOVOSCAMPOS
FROM USRXCAM ORIGEM
INNER JOIN (SELECT NOME, CODCFGUSR FROM CFGUSR WHERE NOME IN ('PCM', 'OPERACAO', 'COG')) DESTINO ON 0=0 
WHERE ORIGEM.NOMCAM LIKE '%USER_EXCESS_ORDSERV%'
AND ORIGEM.NOME  = 'BEETHOVEN.NUNES'
AND DESTINO.NOME NOT IN (SELECT NOME FROM USRXCAM WHERE NOMCAM LIKE '%USER_EXCESS_ORDSERV%')
 
 /* ----------------------------------------------------- -----------------------------------------------------------------*/
INSERT INTO CAMPOSTELA(CODEMP, COMPONENTE, NOMCAM, NOME, NOMTAB, NOMTEL, CODUSRXCAM)

SELECT 1
      , ORIGEM.COMPONENTE
      , ORIGEM.NOMCAM
      , USRXCAM.NOME
      , ORIGEM.NOMTAB
      , ORIGEM.NOMTEL
      , USRXCAM.CODUSRXCAM
FROM USRXCAM 
INNER JOIN (SELECT COMPONENTE, NOMCAM, NOMTAB, NOMTEL 
            FROM CAMPOSTELA 
            WHERE NOMCAM LIKE 'FKU_USER_EXCESS_ORDSERV' AND NOME = 'BEETHOVEN.NUNES'
            ) ORIGEM ON 0=0 
WHERE USRXCAM.NOME NOT IN (SELECT NOME FROM CAMPOSTELA WHERE NOMCAM LIKE 'FKU_USER_EXCESS_ORDSERV')
AND USRXCAM.NOMTAB='ORDSERV' AND USRXCAM.NOMCAM LIKE 'FKU_USER_EXCESS_ORDSERV'