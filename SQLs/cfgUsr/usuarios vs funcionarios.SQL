SELECT CFGUSR.NOME 'USARIO'
      , CFGUSR.U_CORRIGE 'CORRIGE?'
      , CFGUSR.U_USUADM 'ADMIN?'
      , FUNC.NOME 'NOME'
      , FUNC.CODFUN
      , CFGUSR.NOMEGRUPO
      , FUNC.EMAIL 
FROM CFGUSR
INNER JOIN USRXEMP ON USRXEMP.NOME = CFGUSR.NOME
INNER JOIN FUNC ON USRXEMP.CODFUN = FUNC.CODFUN 
WHERE CFGUSR.ATIVO = 'S'
 